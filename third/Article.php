<?php

namespace third;


class Article
{
    private $user;
    private $text = '';

    public function __construct($user, $text)
    {
        $this->user = $user;
        $this->text = $text;
    }


    /**
     * @return User
     */
    public function getUser() : User
    {
        return $this->user;
    }

    /**
     * @param User $user
     */
    public function setUser(User $user)
    {
        $this->user = $user;
    }

    /**
     * @return string
     */
    public function getText(): string
    {
        return $this->text;
    }

    /**
     * @param string $text
     */
    public function setText(string $text)
    {
        $this->text = $text;
    }
}